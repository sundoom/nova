//////////////////////////////////////////////////////////////////////////

#include "Local.h"

//////////////////////////////////////////////////////////////////////////
// Construction/Destruction
//////////////////////////////////////////////////////////////////////////

static BOOL s_exit = FALSE;

//////////////////////////////////////////////////////////////////////////

I32 main(I32 argc, CHAR** argv)
{
  if(argc < 2)
  {
    NAssert(VA("%s : requires server argument.",argv[0]));
    return -1;
  }

  // init the application
  AppInit(argc-1, &argv[1]);

  // run the application
  while(s_exit == FALSE)
  {
    AppRun();
  }

  // exit the application
  AppExit();
	
	return 0;
}

//////////////////////////////////////////////////////////////////////////

VOID Exit()
{
  s_exit = TRUE;
}

